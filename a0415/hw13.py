import random

menu = {
  "월": ["김치찌개", "된장찌개", "돈까스"], 
  "화": ["비빔밥", "짬뽕", "떡볶이"], 
  "수": ["라멘", "카레", "족발"], 
  "목": ["샐러드", "제육", "김밥"], 
  "금": ["피자", "햄버거", "초밥"], 
  "토": ["냉면", "닭갈비", "갈비찜"],
  "일": ["짜장면", "닭발", "김치볶음밥"]}

day = input("오늘 무슨 요일인가요? (예: 월): ")

if day in menu:
  recommended = random.choice(menu[day])
  print(f"{day}요일 추천 메뉴: {recommended}")
else:
  print("올바른 요일을 입력해주세요. (예: 월, 화, 수...)")